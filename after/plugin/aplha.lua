local config_button = button('c', '  Configuration')
config_button.on_press = function ()
    local path = vim.fn.stdpath('config')
    path = vim.fs.normalize(path)
    vim.cmd.e(path)
    vim.cmd.lcd(path)
    vim.notify_once('Configuring...', vim.log.levels.INFO)
end


